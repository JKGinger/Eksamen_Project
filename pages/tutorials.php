  <div class="gris animals mob">
    <img src="../content/img/Pig.png" alt="gris">
      <p>Pigs are a passive animal. They are good for farming and drops porkschops when killed. To get the pig in love mode you will have to feed them eather carrots, potato or beetroot.</p>
