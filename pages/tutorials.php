<h2 class="overskift">Mob Guide</h2>
<div class="dyr">

<div class="ko animals mob">
<img src="../content/img/Cow.png" alt="ko">
<p>Cows are a passive animal mob in Minecraft. They are sources of leather, beef, steak, and milk. 2 cows can make a calf if they are fed wheat. This is called "love mode"
</p>
</div>

<div class="gris animals mob">
<img src="../content/img/Pig.png" alt="gris">
<p>Pigs are a passive animal. They are good for farming and drops porkschop when killed. To get the pig in love mode you will have to feed them eather carrots, potato or beetroot.
</p>
</div>


<div class="sheep animals">
<img src="../content/img/Sheep.gif" alt="sheep">
<p>Sheeps are passive animals. They drop wool and mutton when killed but can also drop wool if they are cut with a scissors. Love mode requires wheat.
</p>
</div>

<div class="kylling animals mob">
<img src="../content/img/Chicken.png" alt="kylling">
<p>Chicken are passive animals. When killed they become food, but can also drop feathers. To get the chickens in love mode they shall be fed any kind of seed. Sometimes chickens drop eggs.
</p>
</div>
