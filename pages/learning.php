        <p>Games engage both the cognitive and the emotional. It is a widespread misconception that the cognitive part of the brain alone is responsible for learning. In fact, it also can have a strong affective component, with much of decision-making taking place in the limbic system, which is connected to emotion and motivation. Gamification taps into both parts of the brain, helping to achieve optimal learning results.
There is a substantial amount of research pointing to the benefits of using gamification to boost learning and achieve results. A 2013 study published in the International Journal of Science and Nature found that playing games can give a healthy 60-year-old the mental capabilities of someone 25 years younger, with the effects lasting for six months, even after individuals stopped playing the game.
Another study, by the University of Colorado Denver Business School, found that using video games in training led to substantial improvements in performance, particularly when the video games actively engaged participants. The researchers analyzed 65 studies and data from 6,476 trainees, focusing on studies examined post-training outcomes for simulation games. The findings indicated that self-efficacy was 20 percent higher for learners trained with simulation games than for a comparison group, declarative knowledge was 11 percent higher, procedural knowledge was 14 percent higher and retention was 9 percent higher.</p>
 
 
